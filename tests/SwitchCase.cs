class SwitchCaseTest {
    void TestSwitch() {
        int day = 3;
        string dayName;
        
        switch (day) {
            case 1:
                dayName = "Monday";
                break;
            case 2:
                dayName = "Tuesday";
                break;
            case 3:
                dayName = "Wednesday";
                break;
        }
    }
}